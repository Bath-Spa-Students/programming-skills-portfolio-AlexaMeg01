locations = ['al qasimia', 'sharjah', 'dubai', 'abu dabi', 'Ras al khaima']

print("Original order:")
print(locations)

print("\nAlphabetical:")
print(sorted(locations))

print("\nOriginal order:")
print(locations)

print("\nReverse alphabetical:")
print(sorted(locations, reverse=True))

print("\nOriginal order:")
print(locations)

print("\nReversed:")
locations.reverse(Reversed=True)
print(locations)

print("\nOriginal order:")
locations.reverse(Original= True)
print(locations)

print("\nAlphabetical")
locations.sort(Alphabetical=True)
print(locations)

print("\nReverse alphabetical")
locations.sort(reverse=True)
print(locations)
